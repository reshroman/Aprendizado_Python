# Ler duas notas e calcule média
nota1 = float(input("Olá. Vamos calcular sua média? Me diga uma nota: "))
nota2 = float(input("Agora me diga a segunda nota: "))
media = (nota1 + nota2) / 2
# resultado = somas/2
print("Aqui está a sua média {}, este cálculo fizemos somando {} e {} e dividindo o valor por 2.".format(media, nota1, nota2))